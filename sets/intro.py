# Sets are unordered , unchangeable and unindexed 

my_set = {1 , 2 , 3}
print(my_set)

# Duplicate values are ignored
# True and 1 are considered same value and as duplicates 

# Can contain different data types 
set1 = { 1 , "aarushi" , True , 2 , 1 , False}
print(set1)
print(len(set1))
print(type(set1))

# use of set() constructor to make a set 
set2 = set((1 , 2 , "aarushi" , True))
print(set2)
