# sort() method is used to sort the list alphanumerically or 
# in ascendin(default) or descending order

fruits = ["cherry" , "grapes" , "guava" , "litchi"]
fruits.sort()
print(fruits)

list = [50 , 29 , 10 , 34 , 2]
list.sort()
print(list)

# sorting in descending order
list = [ 20 , 49 , 30 , 15 , 99]
list.sort(reverse = True)
print(list)