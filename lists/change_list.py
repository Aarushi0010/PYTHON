# change item value 
list = ["apple" , "orange" , "banana"]
list[1] = "watermelon"
print(list)

# change range of item values
list = [1 , 2 ,3 ,4 ,5, 6 ,7]
list[2:5] = [10 , 11 , 12]
print(list)

# replace second item with two new items 
list[1:2] = ["apple" , "watermelon"]
print(list)

# replace 2nd and 3rd item with one item 
list[1:3] = ["sugar"]
print(list)