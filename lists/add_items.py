# using append() method
list = ["apple" , "mango" , "cherry"]
list.append("melon")
print(list)

# using insert() method 
list.insert(2 , "strawberry")
print(list)

# using extend() method
pet_list = ["dog" , "cat" , "rabbit"]
wild_list = ["elephant" , "tiger"]
wild_list.extend(pet_list)
print(wild_list)