# To print string in upper case 

a = "hello world!"
print (a.upper())

# To print string in lower case

a = "HELLO WORLD!"
print(a.lower())

# To remove white spaces in the string 

a = " Hello World !"
print(a.strip())

# Replace string 
a = "Hello World!"
print( a.replace("H" , "J"))

# Split string 
a = "hello , World"
print(a.split(","))