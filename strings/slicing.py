#by using range of characters in the string 
#note that the last position is not included

b = "learning python"
print(b[2:5])

#to print from start till defined position 
x = "An apple a day!"
print(x[:9])

#slice till the end , including the starting position 
x = "An apple a day!"
print(x[4:])

#using negative indexing 
x = "An apple a day!"
print(x[-8 : -2])