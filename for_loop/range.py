# Range() function :
# It is used to loop through the code for a specified number of times 
# It starts wth 0 by default and increments by 1 (by default)

for x in range(6):
    print(x)

# We can also specify the starting point for the loop 

for x in range(2,7):
    print(x)

# We can also specify the increment value by using a third parameter

for x in range(2 , 10 , 3):
    print(x)